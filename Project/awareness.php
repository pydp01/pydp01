        <p id="fpara">Discrimination against LGBTQ workers is pervasive and has negative impacts on their lives. A 2014 report from the Movement Advancement Project and the Center for American Progress estimated that between 8 and 17 percent of lesbian, gay, and bisexual workers are denied employment or unfairly fired on the basis of their sexual orientation; this number rose to 13 to 47 percent for transgender workers. For LGBTQ people of color, workplace discrimination is an even more common experience, with nearly 1 in 3 reporting that they have experienced discrimination when applying for a job because of their LGBTQ identity.
        Title VII’s protections both apply to and extend beyond hiring and firing. The statute makes it “unlawful” for employers “to fail or refuse to hire or to discharge any individual, or otherwise to discriminate against any individual with respect to his compensation, terms, conditions, or privileges of employment.” And although the Supreme Court’s Bostock decision responds to a concern from the employers that the decision will prohibit sex-segregated spaces—stating that “we do not purport to address bathrooms, locker rooms, or anything else of the kind”</p>
        <p id="spara">this does not mean that the court’s decision does not extend to access to existing sex-segregated spaces for transgender workers. The question of whether equal access to facilities is protected under Title VII has been resolved in other contexts and should apply the same for transgender employees. In other words, while Bostock does not resolve whether the existence of sex-segregated bathrooms or locker rooms in an office violates Title VII’s sex discrimination prohibitions, prohibiting a transgender woman from using the women’s restroom violates Title VII’s prohibition on discrimination with respect to the privileges of employment.In addition to facilities access, compensation discrimination is prohibited under Title VII. This includes not only wages but also benefits such as sick and vacation leave, insurance, overtime pay, and retirement programs. While the wide range of protections afforded to workers under Title VII is clear in case law, and while Equal Employment Opportunity Commission decisions make clear that these protections apply to LGBTQ workers, the Trump administration’s hostility toward protections for LGBTQ people—indeed, its open animus toward transgender people—calls into question its willingness to enforce the statute’s full protections.
        </p>
