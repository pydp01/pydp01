        <p class="para">The International Gay & Lesbian Aquatics Association (known as IGLA) is the international governing body for predominantly LGBT+ aquatics clubs, representing the sports of swimming, diving, artistic swimming, water polo, and open water swimming.Member clubs are from 16 countries, with the majority of clubs in the United States and Canada. Past presidents of the association have included Scott Kohanowski (of Team New York Aquatics) and Gareth Johnson (of Out to Swim London).
            IGLA is one of the key organizations involved in the licensing of the Gay Games, which are held every four years. In years in which the Gay Games are not held, IGLA holds an international competition open to its member clubs. The organization is a part of the larger movement of a niche travel industry, LGBTQ sports tourism.
            IGLA began as the West Coast Gay Swim League in 1987 with teams from Berkeley, New York, San Diego, San Francisco, Seattle, Vancouver, and West Hollywood. Members of these teams had participated in Gay Games II and wanted to continue competing annually. After the first West Coast Gay Swim League Championship, hosted in March 1987 by the Different Strokes Swim Team in San Diego, the Seattle team hosted the Northwest Gay & Lesbian Sports Festival. At this festival, it was decided that the League would be renamed the International Gay and Lesbian Aquatics Association in hopes to grow beyond the US and Canada. By Gay Games III, IGLA was officially a part of the Games organizing.</p>
